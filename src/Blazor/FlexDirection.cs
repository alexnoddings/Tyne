namespace Tyne.Blazor;

/// <summary>
///     The direction which items are placed in a flex container.
/// </summary>
public enum FlexDirection
{
    /// <summary>
    ///     Items will be placed vertically in a column.
    /// </summary>
    Column,
    /// <summary>
    ///     Items will be placed horizontally in a row.
    /// </summary>
    Row
}
